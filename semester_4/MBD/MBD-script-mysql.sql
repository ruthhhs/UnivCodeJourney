-- Drop tables jika sudah ada
DROP TABLE IF EXISTS jumlahpesan;
DROP TABLE IF EXISTS menu;
DROP TABLE IF EXISTS pembayaran;
DROP TABLE IF EXISTS pembeli;
DROP TABLE IF EXISTS pesanan;

-- Create tables
CREATE TABLE jumlahpesan (
    jumlah INT NOT NULL,
    harga_kumulatif DECIMAL(8,2),
    menu_id_menu VARCHAR(5) NOT NULL,
    pesanan_id_pesanan VARCHAR(5) NOT NULL,
    PRIMARY KEY (menu_id_menu, pesanan_id_pesanan)
);

CREATE TABLE menu (
    id_menu VARCHAR(5) NOT NULL,
    nama_menu VARCHAR(50),
    harga DECIMAL(8,2) NOT NULL,
    PRIMARY KEY (id_menu)
);

CREATE TABLE pembeli (
    nama_pembeli VARCHAR(50) NOT NULL,
    id_pembeli VARCHAR(6),
    PRIMARY KEY (id_pembeli)
);

CREATE TABLE pesanan (
    id_pesanan VARCHAR(5) NOT NULL,
    total_harga DECIMAL(8,2),
    PRIMARY KEY (id_pesanan)
);

CREATE TABLE pembayaran (
    id_pembayaran VARCHAR(5) NOT NULL,
    waktu DATE,
    pembeli_id_pembeli VARCHAR(6) NOT NULL,
    pesanan_id_pesanan VARCHAR(5) NOT NULL,
    PRIMARY KEY (id_pembayaran),
    UNIQUE (pesanan_id_pesanan)
);

-- Foreign Keys
ALTER TABLE jumlahpesan
    ADD CONSTRAINT jumlahpesan_menu_fk 
    FOREIGN KEY (menu_id_menu) REFERENCES menu(id_menu);

ALTER TABLE jumlahpesan
    ADD CONSTRAINT jumlahpesan_pesanan_fk 
    FOREIGN KEY (pesanan_id_pesanan) REFERENCES pesanan(id_pesanan);

ALTER TABLE pembayaran
    ADD CONSTRAINT pembayaran_pembeli_fk 
    FOREIGN KEY (pembeli_id_pembeli) REFERENCES pembeli(id_pembeli);

ALTER TABLE pembayaran
    ADD CONSTRAINT pembayaran_pesanan_fk 
    FOREIGN KEY (pesanan_id_pesanan) REFERENCES pesanan(id_pesanan);

